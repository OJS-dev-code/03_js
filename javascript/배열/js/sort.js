const array1=[1,30,4,21,10000];
console.log(array1); //[1, 30, 4, 21, 10000]
console.log(array1.sort()); //[1, 10000, 21, 30, 4]

//sort () -> 문자열로 바꾼 후 정렬
//1 === "1";
//30 === "30"; //앞자리 수가 3이라서 30이 4보다 앞
//4 === "4";
//10000 ==="10000" //앞자리 수가 1이라서 10000이 1 뒤

//array1.sort((a,b)=>a-b)
//오름차순으로 정렬
console.log(array1.sort((a,b)=>a-b)) //[1, 4, 21, 30, 10000]
//내림차순으로 정렬
console.log(array1.sort((a,b)=>b-a))